<h2>Orange Juice Blues (Blues for Breakfast)</h2>
<p>
	I have a hard time handing out warnings. I'll just slide on out the door. Cuz
	I'm tired of everything. Being beautiful, beautiful. And I ain't coming back
	no more.
</p>
<p>
	In the dime stores and bus stations. People talk of situations. Read books,
	repeat quotations. Draw conclusions on the wall. Some speak of the future. My
	love she speaks softly. She knows there’s no success like failure. And that
	failure’s no success at all.
</p>
<p>
	The whole world is asleep. You can look at it and weep. Few things you find
	are worthwhile. And though I don’t ask for much. No material things to touch.
	Lord, protect my child.
</p>
<h2>Make You Feel My Love</h2>
<p>
	When the evening shadows and the stars appear. And there is no one there to
	dry your tears. I could hold you for a million years. To make you feel my
	love.
</p>
<p>
	I ain’t gonna work on Maggie’s farm no more. No, I ain’t gonna work on
	Maggie’s farm no more. Well, I wake in the morning. Fold my hands and pray for
	rain. I got a head full of ideas. That are drivin’ me insane. It’s a shame the
	way she makes me scrub the floor. I ain’t gonna work on Maggie’s farm no more.
</p>
<p>
	Oh, the flowers of indulgence and the weeds of yesteryear. Like criminals,
	they have choked the breath of conscience and good cheer. The sun beat down
	upon the steps of time to light the way. To ease the pain of idleness and the
	memory of decay.
</p>
