<p>
	I’s out there paintin’ on the old woodshed. When a can a black paint it fell
	on my head. I went down to scrub and rub. But I had to sit in back of the tub.
	(Cost a quarter. And I had to get out quick . . . Someone wanted to come in
	and take a sauna)
</p>
<p>
	You’re a man of the mountains, you can walk on the clouds. Manipulator of
	crowds, you’re a dream twister. You’re going to Sodom and Gomorrah. But what
	do you care? Ain’t nobody there would want to marry your sister. Friend to the
	martyr, a friend to the woman of shame. You look into the fiery furnace, see
	the rich man without any name.
</p>
<h2>Like a Rolling Stone</h2>
<p>
	You never turned around to see the frowns on the jugglers and the clowns. When
	they all come down and did tricks for you. You never understood that it ain’t
	no good. You shouldn’t let other people get your kicks for you. You used to
	ride on the chrome horse with your diplomat. Who carried on his shoulder a
	Siamese cat. Ain’t it hard when you discover that. He really wasn’t where it’s
	at. After he took from you everything he could steal.
</p>
